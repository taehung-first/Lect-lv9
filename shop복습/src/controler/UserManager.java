package controler;

import java.util.Random;
import java.util.Scanner;
import java.util.Vector;

import models.User;
public class UserManager {
	Scanner sc = new Scanner(System.in);
	Random rn = new Random();
	public static UserManager instance = new UserManager();
	public Vector<User> userList = new Vector<User>();
	public static int log;
	public UserManager(){
		
		inti();
	}
	void inti() {
		for(int i=0; i<10;i++) {
			String []a = { "김", "박", "이", "최", "정", "오" };
			String []b = { "철", "병", "만", "여", "아", "영" };
			String []c = { "수", "욱", "수", "정", "름", "희" };
			
			int r = rn.nextInt(a.length);
			String name = a[r];
			r = rn.nextInt(b.length);
			name +=b[r];
			r = rn.nextInt(c.length);
			name +=c[r];
			
			User temp = new User(name, r);
			userList.add(temp);
		}
	}
	public void join() {
		int check = -1;
		System.out.println("아이디를 입력:");
		String id = sc.next();
		
		for(int i=0;i<userList.size();i++) {
			if(id.equals(userList.get(i).getId())) {
				check = i ;
				break;
			}
		}
		if(check==-1) {
			System.out.println(id +"님 가입을 환영합니다.");
			User temp = new User(id, check);
		userList.add(temp);
		}
		else {
			System.out.println("중복되는 아이디");
			return;
		}
	}
	public void remove() {
		int check =-1;
		System.out.println("아이디 입력:");
		String id = sc.next();
		for(int i=0;i<userList.size();i++) {
			if(id.equals(userList.get(i).getId())) {
				check =i;
				break;
			}
		}
		if(check ==-1) {
			System.out.println("아이디를 확인하세요");
		}else {
			System.out.println(id+"님 탈퇴되었습니다.");
			userList.remove(check);
		}
	}
	public boolean logIn() {
		int check=-1;
		System.out.println("아이디 입력:");
		String id = sc.next();
		for(int i=0;i<userList.size();i++) {
			if(id.equals(userList.get(i).getId())) {
				check =i;
				break;
			}
		}
		if(check ==-1) {
			System.out.println("존재하지 않은 아이디입니다.");
			return false;
		}else {
			System.out.println(id +"님 환영합니다.");
			log = check;
			return true;
		}
	}
	
	public void logOut() {
		if(log != -1) {
			System.out.println(userList.get(log).getId()+"님 로그아웃");
			log = -1 ;
		}
		else {
			System.out.println("로그인이 되어있지 않습니다.");
			return;
		}
	}
	public void print() {
		for(int i=0;i<userList.size();i++) {
			System.out.println(i);
			userList.get(i).print();
		}
	}
	public void printID() {
		for(int i=0;i<userList.size();i++) {
			System.out.println("회원 ID"+userList.get(i).getId()+"잔액"+userList.get(i).getMoney());
		}
	}
	
	
}
